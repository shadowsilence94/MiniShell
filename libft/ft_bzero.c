#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*x;

	x = s;
	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		x[i] = '\0';
		i++;
	}
}
