#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*x;

	x = s;
	i = 0;
	if (!s)
		return (NULL);
	while (i < n)
	{
		x[i] = c;
		i++;
	}
	return (x);
}
